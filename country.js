const primaryCountriesSet = new Set(primaryCountries)

const allCountries = [...primaryCountries, ...allCountriesSorted.filter(country => !primaryCountriesSet.has(country))]

const allCountriesSet = new Set(allCountries);

function getPrettyCountry(country){
  if (!country) return

  switch(country){
    case 'KOREA_SOUTH':
      return "South Korea"
    case 'KOREA_NORTH':
      return "North Korea"
    default:
      return country.split("_").map(word => { return word[0].toUpperCase() + word.slice(1).toLowerCase()} ).join(" ")
  }
}

function safeCountry(country){
  if (!country) return
  const item = country.toUpperCase().trim()
  if (!allCountriesSet.has(item)){ throw new Error("Invalid Country") }
  return item
}

